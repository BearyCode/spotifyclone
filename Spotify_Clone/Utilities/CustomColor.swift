import UIKit

extension UIColor {
    class var spotifyBackground: UIColor {
        return UIColor(red: 18/255, green: 18/255, blue: 18/255, alpha: 1.0)
    }
    
    class var filterBackground: UIColor {
        return UIColor(red: 41/255, green: 41/255, blue: 41/255, alpha: 1.0)
    }
    
    class var spotifyGreen: UIColor {
        return UIColor(red: 101/255, green: 211/255, blue: 110/255, alpha: 1.0)
    }
}
